main(){
    // arithmetic operators
    int a = 10 + 5;   // 15
    int b = 10 - 5;   // 5
    int c = 10 * 5;   // 50
    double d = 10 / 5;  // 2.0
    int e = 10 % 5;   // 0
    // comparison operators
    bool f = 10 > 5;   // true
    bool g = 10 < 5;   // false
    bool h = 10 >= 5;  // true
    bool i = 10 <= 5;  // false
    bool j = 10 == 5;  // false
    bool k = 10 != 5;  // true
    // logical operators
    bool l = true && false;  // false
    bool m = true || false;  // true
    bool n = !true;   // false
    // assignment operators
    int o = 10;
    o += 5;   // 15
    o -= 5;   // 10
    o *= 5;   // 50
    o /= 5;   // 10
    o %= 5;   // 0
}